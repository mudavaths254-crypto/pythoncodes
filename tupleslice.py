fruits=('apple','banana','mango','grapes','orange')
print(fruits[0])
print(fruits[2])
print(fruits[-1])
print(fruits[-2])

#slicing tuples
fruits=('apple','banana','mango','grapes','orange')
print(fruits[1:3])
# slice from beginning up to index 3
print(fruits[:3])
#slice from index 2 to end
print(fruits[2:])
#step slicing every second element
print(fruits[::2])
# reverse tuple
fruits=('apple','banana','mango','grapes','orange')
print(fruits[::-1])
